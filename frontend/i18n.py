_CATALOGS: dict[str, dict[str, str]] = {"es": ES, "pt": PT}


def set_language(lang: str | None) -> None:
    """Define o idioma atual do processo (es/pt). Inválido -> default."""
    global _lang
    _lang = lang if lang in _CATALOGS else DEFAULT_LANG


def get_language() -> str:
    return _lang


def t(key: str, **kwargs: Any) -> str:
    """Traduz a chave no idioma atual. Fallback: idioma -> es -> chave."""
    text = _CATALOGS.get(_lang, ES).get(key) or ES.get(key) or key
    if kwargs:
        try:
            text = text.format(**kwargs)
        except Exception:
            pass
    return text
